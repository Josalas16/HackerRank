# Solve me First

# Function prototype:
# int solveMeFirst(int a, int b); where

# a is the first integer input
# b is the second integer input

# Return Values
# sum of the above two integers

# Sample Input
# a = 2
# b = 3

# Sample Output
# 5

# Explanation
# The sum of the two integers a and b is computed as: 2 + 3 = 5.